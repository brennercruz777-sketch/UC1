voto = int(input('Digite o número do seu candidato (15, 25 ou 36): '))
if voto ==15:
    print('Seu candidato escolhido foi Juquinha')
elif voto ==25:
    print('Seu candidato escolhido foi Luizinho')
elif voto ==36:
    print('Seu candidato escolhido foi Aninha')
else:
    print('Número inválido')
